function pluginCount()
	local i, t, popen = 0, {}, io.popen
	for filename in popen("ls ~/.local/share/nvim/site/pack/packer/start/"):lines() do
		i = i + 1
	end
	for filename in popen("ls ~/.local/share/nvim/site/pack/packer/opt/"):lines() do
		i = i + 1
	end
	return i
end

